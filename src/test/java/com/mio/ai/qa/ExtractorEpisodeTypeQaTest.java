    private static ExtractorLlmClient extractor;
    private static final AtomicInteger passed = new AtomicInteger(0);
    private static final AtomicInteger failed = new AtomicInteger(0);
    private static final List<String> failures = new ArrayList<>();

    @BeforeAll
    static void setUp() {
        String apiKey = System.getenv("OPENAI_API_KEY");
        Assumptions.assumeTrue(
                apiKey != null && !apiKey.isBlank(),
                "OPENAI_API_KEY 환경변수 없음 — LLM 통합 테스트 skip"
        );
        extractor = new ExtractorLlmClient(
                new OpenAiLlmClient(apiKey, HttpClient.newHttpClient(), new ObjectMapper()),
                new ObjectMapper()
        );
    }

    @AfterAll
    static void printStats() {
        int total = passed.get() + failed.get();
        System.out.printf("%n═══════════════════════════════════════%n");
        System.out.printf("  ExtractorEpisodeType QA 결과%n");
        System.out.printf("  통과: %d / %d  (%.1f%%)%n",
                passed.get(), total, total > 0 ? passed.get() * 100.0 / total : 0.0);
        System.out.printf("═══════════════════════════════════════%n");
        if (!failures.isEmpty()) {
            System.out.println("  실패 목록:");
            failures.forEach(f -> System.out.println("    " + f));
        }
    }

    @ParameterizedTest(name = "[{0}] {2}")
    @MethodSource("scenarios")
    @DisplayName("episodeType 분류")
    void classifyEpisodeType(String id, String expectedType, String description, String summary) {
        ExtractorResult result = extractor.extract(summary);
        System.out.printf("[%s] expected=%-14s actual=%-14s emotion=%-10s score=%s thoughts=%d  — %s%n",
                id, expectedType, result.episodeType(), result.dominantEmotion(),
                result.emotionScore(), result.thoughts().size(), description);
        try {
            assertThat(result.episodeType())
                    .as("[%s] %s", id, description)
                    .isEqualTo(expectedType);
            passed.incrementAndGet();
        } catch (AssertionError e) {
            failed.incrementAndGet();
            failures.add(String.format("%s | expected=%-14s actual=%-14s | %s",
                    id, expectedType, result.episodeType(), description));
            throw e;
        }
    }

